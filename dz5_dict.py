# 2) Дан словарь с числовыми значениями. Необходимо их все перемножить и вывести на экран.
#   Результаты выведите на экран.
_price= {'a': 34, 'b':56 ,'c':89 }
_result=1
for key in _price:
    _result = _result * _price[key]
print(_result)


