# 4) Даны два списка одинаковой длины. Необходимо создать из них словарь таким образом,
#   чтобы элементы первого списка были ключами,
#   а элементы второго — соответственно значениями нашего словаря.
#   Результаты выведите на экран.

_list1 = [1036, 2214, 5042, 71103, 4114, 3010, 1001]
_list2 = [20, 2, 5, 80, 44, 3, 50]
_dict4 = {}
for i in range(len(_list1)):
    _dict4[_list1[i]] = _dict4.get(_list1[i], 0) + _list2[i]
print(_dict4)